export function getTermByLabel(label: string): TermRecord | undefined {
  const lower = label.trim().toLowerCase();
  return TERMS.find(
    (t) =>
      t.labelEn.toLowerCase() === lower ||
      t.labelEs.toLowerCase() === lower ||
      t.id === lower.replace(/\s+/g, "-")
  );
}
